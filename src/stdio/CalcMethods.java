package stdio;

class CalcMethods {

    double add(double a, double b) {
        return a + b;
    }

    double subtract(double a, double b) {
        return a - b;
    }

    double multiply(double a, double b) {
        return a * b;
    }

    int divide(double a, double b) {
        return (int) a / (int) b;
    }


}
